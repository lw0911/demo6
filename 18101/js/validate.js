/**************************************正则表达式****************************************/
//手机验证--正则
var TEL_REG = /^(1[3,4,5,7,8])\d{9}$/;
//座机验证--正则

//邮箱验证--正则
var EMAIL_REG = /\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*/;
//密码验证--正则

//中文验证--正则


/**************************************验证函数****************************************/
//匹配phone
function isPhone(str){
	return TEL_REG.test(str);
}
//匹配邮箱
function isEmail(str){
	return EMAIL_REG.test(str);
}